let currentLang = localStorage.getItem('lang') || 'ru';
let currentTheme = localStorage.getItem('theme') || 'dark';

document.addEventListener('DOMContentLoaded', () => {
    setTheme(currentTheme);
    setLanguage(currentLang);

    // Переключатель темы
    const themeBtn = document.getElementById('theme-toggle');
    if(themeBtn) {
        themeBtn.addEventListener('click', () => {
            currentTheme = currentTheme === 'dark' ? 'light' : 'dark';
            setTheme(currentTheme);
        });
    }

    // Переключатель языка
    const langBtn = document.getElementById('lang-toggle');
    if(langBtn) {
        langBtn.addEventListener('click', () => {
            currentLang = currentLang === 'ru' ? 'en' : 'ru';
            setLanguage(currentLang);
        });
    }

    // ИНТЕРАКТИВНАЯ ЛОГИКА ДЛЯ СМОКЕРА И ДЫМА
    const smoker = document.getElementById('smoker-trigger');
    const smokeZone = document.getElementById('smoke-zone');

    if(smoker && smokeZone) {
        smoker.addEventListener('click', () => {
            smoker.classList.toggle('open');

            if(smoker.classList.contains('open')) {
                // Генерируем клубы дыма циклично, пока открыт
                let smokeInterval = setInterval(() => {
                    if(!smoker.classList.contains('open')) {
                        clearInterval(smokeInterval);
                        return;
                    }
                    createSmokePuff(smokeZone);
                }, 200);
            }
        });
    }

    // ЛИСТАНИЕ СТРАНИЦ МЕНЮ
    const page1 = document.getElementById('page-1');
    const page2 = document.getElementById('page-2');
    const prevBtn = document.getElementById('prev-page');
    const nextBtn = document.getElementById('next-page');

    if(page1 && page2) {
        function checkButtons() {
            prevBtn.disabled = page1.classList.contains('active');
            nextBtn.disabled = page2.classList.contains('active');
        }

        prevBtn.addEventListener('click', () => {
            page2.classList.remove('active');
            page1.classList.add('active');
            checkButtons();
        });

        nextBtn.addEventListener('click', () => {
            page1.classList.remove('active');
            page2.classList.add('active');
            checkButtons();
        });

        checkButtons();
    }
});

// ГЕНЕРАТОР КЛУБОВ ДЫМА
function createSmokePuff(zone) {
    const puff = document.createElement('div');
    puff.classList.add('smoke-puff');
    
    // Слегка рандомизируем размеры и начальное положение дыма
    const size = Math.random() * 30 + 20;
    puff.style.width = `${size}px`;
    puff.style.height = `${size}px`;
    puff.style.left = `${Math.random() * 60 + 80}px`;
    
    zone.appendChild(puff);

    // Удаляем элемент после завершения анимации
    setTimeout(() => {
        puff.remove();
    }, 2000);
}

function setTheme(theme) {
    document.documentElement.setAttribute('data-theme', theme);
    localStorage.setItem('theme', theme);
    const themeIcon = document.querySelector('#theme-toggle i');
    if(themeIcon) {
        themeIcon.className = theme === 'dark' ? 'fas fa-sun' : 'fas fa-moon';
    }
}

function setLanguage(lang) {
    localStorage.setItem('lang', lang);
    currentLang = lang;
    
    const langBtn = document.getElementById('lang-toggle');
    if(langBtn) langBtn.innerText = lang === 'ru' ? 'EN' : 'RU';

    document.querySelectorAll('[data-lng]').forEach(element => {
        const key = element.getAttribute('data-lng');
        if(dictionary[lang][key]) {
            element.innerText = dictionary[lang][key];
        }
    });
}